    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
      // TODO add your handling code here:
      Conectar c = new Conectar();
      Connection cn = c.Escola();

      try
        {

          if (jtNome.getText().isEmpty() == false)
            {
               PreparedStatement pst = cn.prepareStatement("INSERT INTO Cursos (nomecurso) VALUES(?)");

               pst.setString(1,jtNome.getText());

               int a = pst.executeUpdate();

               if (a > 0)
                 {
                    JOptionPane.showMessageDialog(null,"Cadastrado com Sucesso!","Informação",JOptionPane.INFORMATION_MESSAGE);
                    mostrardados("");
                    jtNome.setText("");
                    jtNome.grabFocus();
                 }
               else
                 {
                    JOptionPane.showMessageDialog(null,"Error ao cadastrar!","Erro",JOptionPane.ERROR_MESSAGE);
                    jtNome.grabFocus();
                 }
           }
          else
            {
               JOptionPane.showMessageDialog(null,"Informe o nome do Curso!","Aviso",JOptionPane.WARNING_MESSAGE);
               jtNome.grabFocus();
            }
        }
      catch(Exception e)
        {

        }
    }//GEN-LAST:event_jbSalvarActionPerformed

