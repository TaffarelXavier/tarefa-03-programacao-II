    void mostrardados(String valor){
        
      Conectar cc = new Conectar();
      Connection cn = cc.Escola();
      
      DefaultTableModel modelo = new DefaultTableModel(){
          
        //método que impede o mode de edição na Tabela....
        @Override
        public boolean isCellEditable(int linhas, int colunas) {
          if (colunas == 2)
            return true;
          else
            return false;
       }
   };
    
    modelo.addColumn("Código");
    modelo.addColumn("Nome do Curso");
          
    jtTabela.setModel(modelo);
      
    String sql = "";
      
    if (valor.equals(""))
      sql = "SELECT * FROM Cursos";
    else
      sql = "SELECT * FROM Cursos WHERE (codcurso='"+valor+"' OR nomecurso='"+valor+"')";
        
    String[]dados = new String [2];
        
    try
      {
        Statement st=cn.createStatement();
        ResultSet rs=st.executeQuery(sql);
        while(rs.next())
          {
            dados[0] = rs.getString(1);
            dados[1] = rs.getString(2);

            modelo.addRow(dados);
          }
        jtTabela.setModel(modelo);
      }
    catch(SQLException ex)
      {
        Logger.getLogger(dados.class.getName()).log(Level.SEVERE,null,ex);
      }  
    
    jbNovo.setEnabled(false);
    jbRemover.setEnabled(false);
    jbAlterar.setEnabled(false);
    jbBuscar.setEnabled(false);
  }
    
    
